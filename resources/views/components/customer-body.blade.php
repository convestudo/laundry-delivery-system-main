<script type="text/javascript">
    $(document).ready(function () {
        $('.ry-accordion-toggle').on('click', function (e) {
            e.preventDefault();

            var $item = $(this).closest('.ry-accordion-item');
            var $content = $item.find('.ry-accordion-content');

            // Toggle current item
            $content.slideToggle(200).toggleClass('w--open');

            // Optionally close other open items (accordion behavior)
            $item.siblings('.ry-accordion-item').find('.ry-accordion-content').slideUp(200).removeClass('w--open');
        });
    });

</script>

@endpush
